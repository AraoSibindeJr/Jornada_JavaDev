package javacore.introducaoclasses.dominio;

public class Estudante {
    // Introducao a POO Programacao Orientada a Objecto
    /*
    * - Objecto: Instancia de uma classe;
    * - Classe: Contem os atributos e metodos necessarios para criacao de um objecto
    * - Atributo: Caracteristicas de um objecto. (O que o objecto tem)
    * - Metodos: Status de um objecto. (O que o objecto faz)
    * */

    // Declaracao de atributos: (O que o objecto tem)
    public short idade;
    public String nome;
    public int anoMatricula;
    public char sexo;
}
